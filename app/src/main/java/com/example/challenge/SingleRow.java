package com.example.challenge;

public class SingleRow {
    String name;
    String lvl;

    public SingleRow(String name, String lvl) {
        this.name = name;
        this.lvl = lvl;
    }

    public String getName() {

        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public String getLvl() {
        return lvl;
    }

    public void setLvl(String lvl) {
        this.lvl = lvl;
    }
}
